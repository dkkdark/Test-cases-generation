def get_test_case_prompt() -> str:
    return """
        Ты — эксперт по генерации и актуализации тест-кейсов.

        На основе следующей документации или пользовательского запроса:
        ---
        {query}
        ---

        Если передан контекст из Confluence, используй его как дополнительный источник истины:
        Ссылка:
        {doc_url}

        Контекст страницы:
        ---
        {confluence_context}
        ---

        Определи, что нужно сделать с тестовой моделью:
        - создать новый тест-кейс, если это новая фича или сценарий не покрыт;
        - обновить существующий тест-кейс, если запрос меняет уже покрытый сценарий;
        - при необходимости одновременно и обновить существующие тесты, и создать новые.

        Ниже похожие и потенциально затронутые тесты из базы:
        ---
        {data}
        ---

        Ниже дополнительный сигнал от поискового ранжирования:
        ---
        {ranked_candidates}
        ---

        КРИТИЧЕСКИ ВАЖНО:
        - Создавай или обновляй базовые тесты для ключевых сценариев и добавляй соседние важные сценарии, если они действительно самостоятельны.
        - Если сценарий уже покрыт похожим тестом, предпочитай `update`, а не создание дубля.
        - Если запрос добавляет новый независимый сценарий, которого нет в найденных тестах, используй `create`.
        - Если в блоке ранжирования есть кандидат с пометкой `LIKELY_UPDATE`, сначала проверь, можно ли обновить именно его.
        - Если изменение хотя бы частично расширяет или меняет уже существующий сценарий, смещайся в сторону `update`.
        - Используй `update` только если изменение действительно относится к уже существующему сценарию, а не просто похоже по словам.
        - Если все кандидаты помечены как `RELATED` и сценарий по смыслу новый, используй `create`.
        - НЕ пытайся покрыть все детали из документации.
        - Можно предлагать несколько кейсов, если в изменении есть несколько самостоятельных пользовательских сценариев.
        - НЕ ограничивайся одним кейсом, если документ описывает несколько значимых сценариев.
        - Избегай только совсем второстепенных edge cases, редких validation-only и технических error handling сценариев.
        - Один тест = один основной пользовательский сценарий или одно крупное изменение существующего сценария.
        - При `update` верни полную новую версию тест-кейса целиком, а не только дельту.
        - При `update` учитывай предыдущее содержание теста из базы как основу.
        - Для `update` сохраняй прежний `Name` и существующие `Fields`
        - Строго следуй стилю тестов из базы. Оцени длину и количество слов в тесте в каждой секции и следуй примерно такой же структуре.
        - Пиши тесты только на русском языке.
        - После анализа верни результат в трёх группах:
          1. `most_important` — самые важные кейсы, которые стоит сделать в первую очередь.
          2. `less_important` — полезные, но менее критичные кейсы.
          3. `possibly_affected_existing` — существующие кейсы, которые с высокой или средней вероятностью затронуты изменением и требуют проверки или обновления.

        Формат результата (JSON object):
        {{
          "most_important": [
            {{
              "Operation": "create" | "update",
              "Existing ID": number | null,
              "Name": string,
              "Description": string,
              "Precondition": string,
              "Step": list(string),
              "Expected result": string,
              "Fields": list(dict(string))
            }}
          ],
          "less_important": [
            {{
              "Operation": "create" | "update",
              "Existing ID": number | null,
              "Name": string,
              "Description": string,
              "Precondition": string,
              "Step": list(string),
              "Expected result": string,
              "Fields": list(dict(string))
            }}
          ],
          "possibly_affected_existing": [
            {{
              "Operation": "update",
              "Existing ID": number,
              "Name": string,
              "Description": string,
              "Precondition": string,
              "Step": list(string),
              "Expected result": string,
              "Fields": list(dict(string))
            }}
          ]
        }}

        Правила для полей:
        - Для `update` обязательно заполняй `Existing ID` id существующего теста из базы.
        - Для `create` ставь `Existing ID` = null.
        - `Description` — краткое и полезное описание сценария теста. Оно будет сохранено в тест-кейс.
        - В `most_important` помещай главный набор кейсов, без искусственного ограничения до одного-двух тестов.
        - В `less_important` добавляй ещё полезные самостоятельные кейсы, не дублируя `most_important`.
        - В `possibly_affected_existing` включай только уже существующие кейсы из базы, которые вероятно нужно пересмотреть; для них `Operation` должно быть `update`.
        - Не добавляй текст вне JSON.
    """
